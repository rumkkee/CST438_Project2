using System.Collections.Generic;
using UnityEngine;

public static class ListExtensions
{
    // Custom Shuffle.. 
    public static void Shuffle<T>(this List<T> list)
    {
        for (int i = 0; i < list.Count - 1; i++)
        {
            int r = Random.Range(i, list.Count);
            (list[i], list[r]) = (list[r], list[i]);
        }
    }
}